package OutrosTopicosBasicos;

public class Bitwise {
    public static void main(String[] args) {
        // & - Operação E bit a bit.
        // | - Operação OU bit a bit
        // ^ - Operação OU-Exclusivo bit a bit

        int n1 = 89;
        int n2 = 60;

        System.out.println(n1 & n2);
        System.out.println(n1 | n2);
        System.out.println(n1 ^ n2);
    }
}
